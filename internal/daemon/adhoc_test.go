func TestHandleContainerStop_StopsChildren(t *testing.T) {
	d, mockRT := newTestDaemon(nil)

	// Create parent
	d.state.SetResource(&status.ResourceStatus{
		Name:  "parent",
		Type:  status.TypeContainer,
		State: status.StateRunning,
	})

	// Create children
	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-1",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.adhoc": "true", "skiff.parent": "parent"},
	})
	d.adhoc.Track("child-1", "parent", true, 0)

	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-2",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.adhoc": "true", "skiff.parent": "parent"},
	})
	d.adhoc.Track("child-2", "parent", true, 0)

	mux := d.setupRoutes()
	rr := doRequest(mux, "POST", "/v1/containers/parent/stop", nil)

	if rr.Code != http.StatusOK {
		t.Errorf("expected 200, got %d", rr.Code)
	}

	// 3 Stop calls: parent + 2 children
	stops := mockRT.CallsFor("Stop")
	if len(stops) != 3 {
		t.Errorf("expected 3 Stop calls (parent + 2 children), got %d", len(stops))
	}

	// Children should be removed from state (remove=true)
	if _, ok := d.state.GetResource("child-1"); ok {
		t.Error("expected child-1 to be removed from state")
	}
	if _, ok := d.state.GetResource("child-2"); ok {
		t.Error("expected child-2 to be removed from state")
	}
}

