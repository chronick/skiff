func TestHandleContainerRun_WithAllOptions(t *testing.T) {
	d, mockRT := newTestDaemon(nil)
	mux := d.setupRoutes()

	rr := doRequest(mux, "POST", "/v1/containers/run", map[string]interface{}{
		"image":        "ghcr.io/chronick/sandbox:latest",
		"command":      []string{"sleep", "infinity"},
		"volumes":      []string{"/host:/container"},
		"env":          map[string]string{"KEY": "value"},
		"cpus":         2,
		"memory":       "2G",
		"labels":       map[string]string{"purpose": "sandbox"},
		"timeout_secs": 3600,
		"remove":       true,
		"name":         "test-sandbox",
	})

	if rr.Code != http.StatusOK {
		t.Errorf("expected 200, got %d: %s", rr.Code, rr.Body.String())
	}

	// Verify runtime received the correct config
	calls := mockRT.CallsFor("Run")
	if len(calls) != 1 {
		t.Fatalf("expected 1 Run call, got %d", len(calls))
	}
	rtCfg := calls[0].Args.(runtime.ContainerConfig)
	if rtCfg.Image != "ghcr.io/chronick/sandbox:latest" {
		t.Errorf("expected image, got %q", rtCfg.Image)
	}
	if len(rtCfg.Volumes) != 1 || rtCfg.Volumes[0] != "/host:/container" {
		t.Errorf("expected volumes, got %v", rtCfg.Volumes)
	}
	if rtCfg.Env["KEY"] != "value" {
		t.Errorf("expected env KEY=value, got %v", rtCfg.Env)
	}
	if rtCfg.Labels["purpose"] != "sandbox" {
		t.Errorf("expected purpose label, got %v", rtCfg.Labels)
	}
	if rtCfg.Labels["skiff.adhoc"] != "true" {
		t.Errorf("expected skiff.adhoc label, got %v", rtCfg.Labels)
	}

	// Verify tracker knows about it
	if !d.adhoc.IsAdhoc("test-sandbox") {
		t.Error("expected test-sandbox to be tracked as ad-hoc")
	}
}

