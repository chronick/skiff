func TestAdhocTracker_StopChildren(t *testing.T) {
	d, mockRT := newTestDaemon(nil)

	// Set up child containers in state
	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-1",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.adhoc": "true", "skiff.parent": "parent"},
	})
	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-2",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.adhoc": "true", "skiff.parent": "parent"},
	})

	d.adhoc.Track("child-1", "parent", true, 0)
	d.adhoc.Track("child-2", "parent", false, 0)

	d.adhoc.StopChildren("parent")

	// Both children should be stopped
	stops := mockRT.CallsFor("Stop")
	if len(stops) != 2 {
		t.Errorf("expected 2 Stop calls, got %d", len(stops))
	}

	// child-1 (remove=true) should be removed from state
	if _, ok := d.state.GetResource("child-1"); ok {
		t.Error("expected child-1 to be removed from state (remove=true)")
	}

	// child-2 (remove=false) should be stopped in state
	rs, ok := d.state.GetResource("child-2")
	if !ok {
		t.Fatal("expected child-2 to remain in state (remove=false)")
	}
	if rs.State != status.StateStopped {
		t.Errorf("expected child-2 state=stopped, got %s", rs.State)
	}
}

