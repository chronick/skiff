// --- POST /v1/containers/run ---

func TestHandleContainerRun_Basic(t *testing.T) {
	d, mockRT := newTestDaemon(nil)
	mux := d.setupRoutes()

	rr := doRequest(mux, "POST", "/v1/containers/run", map[string]interface{}{
		"image": "ghcr.io/chronick/sandbox:latest",
	})

	if rr.Code != http.StatusOK {
		t.Errorf("expected 200, got %d: %s", rr.Code, rr.Body.String())
	}

	var resp AdhocRunResponse
	json.NewDecoder(rr.Body).Decode(&resp)

	if resp.Status != "started" {
		t.Errorf("expected status 'started', got %q", resp.Status)
	}
	if resp.Name == "" {
		t.Error("expected non-empty name in response")
	}

	// Verify runtime.Run was called
	calls := mockRT.CallsFor("Run")
	if len(calls) != 1 {
		t.Fatalf("expected 1 Run call, got %d", len(calls))
	}

	// Verify state was updated
	rs, ok := d.state.GetResource(resp.Name)
	if !ok {
		t.Fatal("expected resource in state")
	}
	if rs.State != status.StateRunning {
		t.Errorf("expected running, got %s", rs.State)
	}
	if rs.Labels["skiff.adhoc"] != "true" {
		t.Errorf("expected skiff.adhoc=true label")
	}
}

