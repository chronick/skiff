// --- randomID ---

func TestRandomID_Unique(t *testing.T) {
	ids := make(map[string]bool)
	for i := 0; i < 100; i++ {
		id := randomID()
		if ids[id] {
			t.Errorf("duplicate random ID: %s", id)
		}
		ids[id] = true
		if len(id) != 8 { // 4 bytes = 8 hex chars
			t.Errorf("expected 8 char hex ID, got %d chars: %s", len(id), id)
		}
	}
}
