func TestHandleContainers_FilterByParent(t *testing.T) {
	d, _ := newTestDaemon(nil)

	// Add containers with different parents
	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-a",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.parent": "agent-1"},
	})
	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-b",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.parent": "agent-1"},
	})
	d.state.SetResource(&status.ResourceStatus{
		Name:   "child-c",
		Type:   status.TypeContainer,
		State:  status.StateRunning,
		Labels: map[string]string{"skiff.parent": "agent-2"},
	})
	d.state.SetResource(&status.ResourceStatus{
		Name:  "no-parent",
		Type:  status.TypeContainer,
		State: status.StateRunning,
	})

	mux := d.setupRoutes()
	rr := doRequest(mux, "GET", "/v1/containers?parent=agent-1", nil)

	if rr.Code != http.StatusOK {
		t.Errorf("expected 200, got %d", rr.Code)
	}

	var containers []map[string]interface{}
	json.NewDecoder(rr.Body).Decode(&containers)
	if len(containers) != 2 {
		t.Errorf("expected 2 containers for agent-1, got %d", len(containers))
	}

	// Verify no parent filter returns all containers
	rr2 := doRequest(mux, "GET", "/v1/containers", nil)
	var allContainers []map[string]interface{}
	json.NewDecoder(rr2.Body).Decode(&allContainers)
	if len(allContainers) != 4 {
		t.Errorf("expected 4 total containers, got %d", len(allContainers))
	}
}

